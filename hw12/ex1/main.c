

/*
Вот пошаговое объяснение каждой строки Makefile:

all: prog
*Определяет цель по умолчанию all, которая зависит от prog
*При запуске make без аргументов будет выполнена эта цель


prog: main.o func.o
*Цель prog (исполняемый файл) зависит от двух объектных файлов
*Если main.o или func.o изменились, prog будет пересобран

gcc -o prog main.o func.o
*Команда сборки: линкует объектные файлы в исполняемый prog
*-o prog явно задаёт имя выходного файла


main.o: main.c func.h
*Объектный файл main.o зависит от исходника main.c и заголовочного файла func.h
*При изменении любого из этих файлов main.o будет перекомпилирован

gcc -c -o main.o main.c
Команда компиляции:
*-c - компиляция без линковки
*-o main.o - задаёт имя выходного объектного файла
*main.c - исходный код


func.o: func.c
*Объектный файл func.o зависит только от func.c
(Предполагается, что func.c не зависит от других заголовков)

gcc -c -o func.o func.c
*Аналогично пункту 5: компиляция func.c в объектный файл


clean:
Вспомогательная цель для очистки проекта
Не имеет зависимостей

del *.o
Команда удаления всех объектных файлов (*.o)
Windows-версия команды (в Linux/macOS используйте rm -f *.o)

del main.exe
Удаление исполняемого файла
Важно: должно соответствовать имени в цели prog (лучше использовать del prog или del prog.exe)
*/

