/*
Мы имеем дело с макросами препроцессора в языке C. 
 В данном коде определены два макроса: A и B.

 Макрос A определяется как `B + 1`, а макрос B определяется как `2`.

 В функции main:
   int a;
   a = A + 2;

 Поскольку макросы работают путем текстовой подстановки, выражение `a = A + 2` будет заменено препроцессором на:
   a = B + 1 + 2;

 Затем макрос B заменяется на 2:
   a = 2 + 1 + 2;

 Таким образом, выражение становится:
   a = 2 + 1 + 2;

 Теперь вычислим значение:
   2 + 1 = 3, затем 3 + 2 = 5.

 Поэтому в программе будет выведено:
   a = 5

 Важно: препроцессор не вычисляет выражения, он только подставляет текст. 
 Вычисление происходит на этапе компиляции (или выполнения, если это не константное выражение).
 */


#include <stdio.h>

#define A B + 1  // A заменяется на B + 1
#define B 2      // B заменяется на 2

int main(void)
{
    int a;
    a = A + 2;
		// a = B + 1 + 2;  // Сначала A заменяется на B + 1
		//a = 2 + 1 + 2;  // Затем B заменяется на 2
    printf("a = %d\n", a);
    return 0;
}
