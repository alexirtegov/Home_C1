/*
Этот код демонстрирует использование стандартных предопределённых макросов в языке C, 
которые предоставляют информацию о компиляции и выполнении программы.

Основные макросы и их значение:
__FILE__
Раскрывается в строку с именем текущего исходного файла.
Пример: "main.c"

__DATE__
Содержит дату препроцессинга в формате Mmm dd yyyy.
Пример: "Jun 12 2023"
(Обратите внимание на опечатку в коде: "Date:Q" → лишняя буква Q)

__TIME__
Содержит время препроцессинга в формате hh:mm:ss.
Пример: "14:30:45"

__STDC_VERSION__
Показывает версию стандарта C (целое число типа long).
Значения:
199409L → C95
199901L → C99
201112L → C11
201710L → C17

__LINE__
Содержит номер текущей строки в исходном файле.

__func__
Не макрос, а предопределённый идентификатор, содержащий имя текущей функции.

 */


#include <stdio.h>

void why_me();

int main(void) {
    printf("File: %s\n", __FILE__);         // 1. Вывод имени файла
    printf("Date:Q %s\n", __DATE__);        // 2. Вывод даты препроцессинга (с опечаткой)
    printf("Time: %s\n", __TIME__);         // 3. Вывод времени препроцессинга
    printf("Version: %ld\n", __STDC_VERSION__); // 4. Версия стандарта C
    printf("Line: %d\n", __LINE__);         // 5. Текущая строка (здесь: 8)
    printf("Function: %s\n", __func__);     // 6. Имя текущей функции ("main")
    why_me();                               // 7. Вызов функции why_me()
    return 0;
}

void why_me() {
    printf("Function: %s\n", __func__);     // 8. Имя функции ("why_me")
    printf("Line: %d\n", __LINE__);         // 9. Текущая строка (здесь: 15)
}
