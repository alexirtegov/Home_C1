/*
C20-Скобки

Проверить строку состоящую из скобок "(" и ")" 
на корректность.



Формат входных данных:
На вход подается строка состоящая из символов 
'(', ')' и заканчивающаяся символом '.'. 
Размер строки не более 1000 символов.


Формат результата:
Необходимо напечатать слово YES если скобки 
расставлены верно и NO в противном случае.

*/





/*
озьмём для примера входную строку
(()).

То есть фактически мы будем читать символы последовательно: ‘(’, ‘(’, ‘)’, ‘)’ и затем точку ‘.’.

Сначала balance=0, ok=1.
Читаем первый символ ‘(’: условие c=='(' истинно, выполняется balance++ → balance становится 1.
Читаем второй символ ‘(’: снова balance++ → balance = 2.
Читаем третий символ ‘)’: заходим в ветку else if (c==')'). Проверяем balance==0? Нет (balance=2), поэтому выполняем balance-- → balance = 1.
Читаем четвёртый символ ‘)’: опять c==')'. Баланс не нулевой (balance=1), делаем balance-- → balance = 0.
Читаем пятый символ ‘.’: условие цикла (c=getchar())!=EOF && c!='.' теперь ложно (c=='.'), выходим из цикла.
После цикла идёт проверка if (c!='.') ok=0; — у нас c действительно точка, значит ok остаётся 1.
Ещё одна проверка if (balance!=0) ok=0; — balance уже 0, значит всё в порядке.
Поскольку ok всё ещё равен 1, программа выводит YES.
*/




#include <stdio.h>

int main(void) {
    int balance = 0;     // текущее число «открытых» скобок, ещё не закрытых
    int c;               // для чтения символов
    int ok = 1;          // флаг: пока 1 — всё ещё может быть правильно

    // Читаем символы до точки или до конца ввода
    while ((c = getchar()) != EOF && c != '.') {
        if (c == '(') {
            // встретили открывающую скобку – увеличиваем баланс
            balance++;
        }
        else if (c == ')') {
            // встретили закрывающую – пытаемся уменьшить баланс
            if (balance == 0) {
                // закрывающая без «пары»
                ok = 0;
                break;
            }
            balance--;
        }
        // прочие символы (если бы они были) мы игнорируем
    }

    // Если цикл прервался до точки (например, EOF) — тоже ошибка
    if (c != '.') {
        ok = 0;
    }
    // Если в конце остались незакрытые скобки — тоже некорректно
    else if (balance != 0) {
        ok = 0;
    }

    // Выводим результат
    if (ok)
        printf("YES\n");
    else
        printf("NO\n");

    return 0;
}
